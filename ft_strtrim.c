#include "libft.h"

char	*ft_strtrim(char const *s1, char const *set)
{
	size_t	i;
	size_t	start;
	size_t	end;
	char	*newstring;

	if (!s1)
		return (NULL);
	if (!set)
		return (ft_strdup(s1));
	start = 0;
	while (s1[start] && ft_strchr(set, s1[start]))
		start++;
	end = ft_strlen(s1);
	while (end > start && ft_strchr(set, s1[end - 1]))
		end--;
	newstring = malloc(sizeof(char) * (end - start + 1));
	if (!newstring)
		return (NULL);
	i = 0;
	while (start < end)
		newstring[i++] = s1[start++];
	newstring[i] = '\0';
	return (newstring);
}
